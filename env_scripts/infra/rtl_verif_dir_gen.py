def parse_arguments() -> argparse.Namespace:
    """
    Parse command-line arguments.
    
    Returns:
        Parsed arguments namespace
    """
    parser = argparse.ArgumentParser(
        description="CAD Verification Directory Structure Generator",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Create unit modules only (creates verification/UnitVerif/*)
  %(prog)s --unit_module_name uart spi i2c
  
  # Create subsystems only (creates verification/SubSystemVerif/*)
  %(prog)s --subsystem_name audio video
  
  # Create system only (creates verification/SysVerif/*)
  %(prog)s --system_name soc
  
  # Create all levels at once
  %(prog)s --unit_module_name uart spi --subsystem_name audio --system_name soc
        """
    )
    
    parser.add_argument(
        '--unit_module_name',
        nargs='+',
        metavar='MODULE',
        help='Unit module name(s) to create structure for'
    )
    
    parser.add_argument(
        '--subsystem_name',
        nargs='+',
        metavar='SUBSYSTEM',
        help='Subsystem name(s) to create structure for'
    )
    
    parser.add_argument(
        '--system_name',
        metavar='SYSTEM',
        help='System name to create structure for (single system only)'
    )
    
    parser.add_argument(
        '--verbose',
        action='store_true',
        help='Enable verbose output'
    )
    
    parser.add_argument(
        '--dry-run',
        action='store_true',
        help='Show what would be created without actually creating anything'
    )
    
    return parser.parse_args()


def validate_arguments(args: argparse.Namespace) -> bool:
    """
    Validate command-line arguments.
    
    Args:
        args: Parsed arguments
    
    Returns:
        True if arguments are valid, False otherwise
    """
    # Check if at least one level is specified
    if not any([args.unit_module_name, args.subsystem_name, args.system_name]):
        print("Error: At least one of --unit_module_name, --subsystem_name, or --system_name must be specified.")
        return False
    
    # Validate names (basic check for valid directory names)
    all_names = []
    if args.unit_module_name:
        all_names.extend(args.unit_module_name)
    if args.subsystem_name:
        all_names.extend(args.subsystem_name)
    if args.system_name:
        all_names.append(args.system_name)
    
    invalid_chars = ['/', '\\', ':', '*', '?', '"', '<', '>', '|']
    for name in all_names:
        if any(char in name for char in invalid_chars):
            print(f"Error: Invalid character in name '{name}'. Names cannot contain: {', '.join(invalid_chars)}")
            return False
    
    return True



    def create_directory_structure(self, base_path: Path, dirs: List[str]) -> None:
        """
        Create directory structure.
        
        Args:
            base_path: Base path where directories will be created
            dirs: List of directory paths to create
        """
        for dir_path in dirs:
            full_path = base_path / dir_path
            try:
                full_path.mkdir(parents=True, exist_ok=True)
                if str(full_path) not in self.created_dirs:
                    self.created_dirs.append(str(full_path))
                    print(f"  ✓ Created directory: {full_path}")
            except Exception as e:
                self.skipped_items.append(str(full_path))
                print(f"  ⚠ Skipped directory: {full_path} (Reason: {e})")
    
    def move_existing_files_to_sim(self, base_path: Path) -> None:
        """
        Move existing Makefile and run_test.py files to sim directory.
        
        Args:
            base_path: Base path where files might exist
        """
        files_to_move = ['Makefile', 'run_test.py']
        sim_path = base_path / 'sim'
        
        for filename in files_to_move:
            source_path = base_path / filename
            target_path = sim_path / filename
            
            if source_path.exists():
                try:
                    # Ensure sim directory exists
                    sim_path.mkdir(parents=True, exist_ok=True)
                    
                    # Move the file
                    shutil.move(str(source_path), str(target_path))
                    print(f"  ✓ Moved {filename} from {base_path} to {sim_path}")
                    
                except Exception as e:
                    self.skipped_items.append(str(source_path))
                    print(f"  ⚠ Failed to move {filename}: {e}")
    
    def create_files(self, base_path: Path) -> None:
        """
        Create files with template content.
        
        Args:
            base_path: Base path where files will be created
        """
        for file_path, content in self.files_to_create:
            full_path = base_path / file_path
            try:
                # Ensure parent directory exists
                full_path.parent.mkdir(parents=True, exist_ok=True)
                
                # Create file if it doesn't exist
                if not full_path.exists():
                    with open(full_path, 'w') as f:
                        f.write(content)
                    
                    # Make Python files executable
                    if file_path.endswith('.py'):
                        os.chmod(full_path, 0o755)
                    
                    self.created_files.append(str(full_path))
                    print(f"  ✓ Created file: {full_path}")
                else:
                    self.skipped_items.append(str(full_path))
                    print(f"  ⚠ File already exists: {full_path}")
            except Exception as e:
                self.skipped_items.append(str(full_path))
                print(f"  ⚠ Failed to create file: {full_path} (Reason: {e})")
    
    def _create_structure_for_level(self, level_name: str, base_dir_name: str, 
                                   names: List[str], dirs: List[str], 
                                   item_type: str) -> None:
        """
        Generic method to create verification structure for any level.
        
        Args:
            level_name: Display name for the level (e.g., "UNIT LEVEL")
            base_dir_name: Base directory name (e.g., "UnitVerif")
            names: List of names to create structures for
            dirs: Directory list to use
            item_type: Type of item being created (e.g., "unit module")
        """
        if not names:
            return
        
        print("\n" + "="*60)
        print(f"Creating {level_name} verification structures")
        print("="*60)
        
        # Create verification folder structure
        verification_base = Path("verification")
        base_dir = verification_base / base_dir_name
        
        for name in names:
            print(f"\n→ Creating structure for {item_type}: '{name}'")
            item_path = base_dir / name
            
            # Create directories
            self.create_directory_structure(item_path, dirs)
            
            # Move existing files to sim folder
            self.move_existing_files_to_sim(item_path)
            
            # Create files
            self.create_files(item_path)
        
        # Create parallel RTL directory structure
        self._create_rtl_structure_for_level(level_name, base_dir_name, names, item_type)
    
    def _create_rtl_structure_for_level(self, level_name: str, base_dir_name: str,
                                       names: List[str], item_type: str) -> None:
        """
        Create RTL directory structure parallel to verification structure.
        
        Args:
            level_name: Display name for the level (e.g., "UNIT LEVEL")
            base_dir_name: Base directory name (e.g., "UnitVerif")
            names: List of names to create structures for
            item_type: Type of item being created (e.g., "unit module")
        """
        print("\n" + "="*60)
        print(f"Creating {level_name} RTL structures")
        print("="*60)
        
        # Create RTL folder structure parallel to verification
        rtl_base = Path("rtl")
        
        # Map verification directory names to RTL directory names
        rtl_dir_mapping = {
            "UnitVerif": "UnitRtl",
            "SubSystemVerif": "SubSystemRtl", 
            "SysVerif": "SysVerifRtl"
        }
        
        rtl_dir_name = rtl_dir_mapping.get(base_dir_name, base_dir_name)
        base_dir = rtl_base / rtl_dir_name
        
        for name in names:
            print(f"\n→ Creating RTL structure for {item_type}: '{name}'")
            item_path = base_dir / name
            
            # Create basic RTL directories (simpler than verification structure)
            rtl_dirs = ['src', 'include', 'tb', 'docs']
            self.create_directory_structure(item_path, rtl_dirs)
            
            # Create filelist.f file
            filelist_path = item_path / 'filelist.f'
            try:
                if not filelist_path.exists():
                    with open(filelist_path, 'w') as f:
                        f.write(self._get_filelist_template())
                    self.created_files.append(str(filelist_path))
                    print(f"  ✓ Created file: {filelist_path}")
                else:
                    self.skipped_items.append(str(filelist_path))
                    print(f"  ⚠ File already exists: {filelist_path}")
            except Exception as e:
                self.skipped_items.append(str(filelist_path))
                print(f"  ⚠ Failed to create file: {filelist_path} (Reason: {e})")
    

#!/usr/bin/env python3
"""
CAD Verification Directory Structure Generator

This script generates standardized directory structures for CAD verification
environments at three hierarchical levels: unit, subsystem, and system.

Author: CAD Verification Team
Date: 2025
"""

import os
import sys
import argparse
import shutil
from pathlib import Path
from typing import List


class CADVerificationStructureGenerator:
    """
    A class to generate CAD verification directory structures.
    """
    
    def __init__(self):
        """Initialize the generator with directory structure definitions."""
        
        # Define common directories for all levels
        self.common_dirs = [
            'agents',
            'env',
            'tests',
            'sequences',
            'tb',
            'assertions',
            'ral',
            'docs',
            'scripts',
            'sim/regression',
            'sim/results',
            'sim/vcs',
            'sim/xcelium',
            'sim/questa'
        ]
        
        # Define level-specific directories
        self.unit_subsystem_dirs = self.common_dirs + ['interface']
        self.system_dirs = self.common_dirs  # System doesn't have interface
        
        # Define files to create
        self.files_to_create = [
            ('sim/regression/run_regression.py', self._get_regression_template()),
            ('sim/run_test.py', self._get_test_template()),
            ('sim/Makefile', self._get_makefile_template()),
            ('sim/vcs/Makefile', self._get_vcs_makefile_template()),
            ('sim/xcelium/Makefile', self._get_xcelium_makefile_template()),
            ('sim/questa/Makefile', self._get_questa_makefile_template()),
            ('filelist.f', self._get_filelist_template())
        ]
        
        # Track created items for reporting
        self.created_dirs = []
        self.created_files = []
        self.skipped_items = []
    
